	private static final void _putRef(String k, String v, int cv) {
		HTML5_NAMED_REFS.put(k, v);
		
		if (k.charAt(k.length() - 1) == ';') {
			Integer iv = Integer.valueOf(cv);
			if (!HTML5_NAMED_REFS_VK.containsKey(iv)) {
				HTML5_NAMED_REFS_VK.put(iv, k);
			}
		}
		
		int uc = (cv & 0xFFFF0000);
		if (uc != 0) {
			HTML5_NAMED_REFS_2DIG.add(Character.valueOf((char)(uc >>> 16)));
		}
	}


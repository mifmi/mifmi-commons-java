	public static final String escapeHTML5Fully(String str, boolean useDecimalNumRef) {
		
		if (str == null || str.isEmpty()) {
			return str;
		}
		
		int len = str.length();
		StringBuilder sb = new StringBuilder(len);
		for (int i = 0; i < len; i++) {
			char c = str.charAt(i);
			if (Character.isHighSurrogate(c)) {
				// Surrogate Pair
				
				if (i + 1 < len) {
					char nc = str.charAt(i + 1);
					if (Character.isLowSurrogate(nc)) {
						int v = (((int)c) << 16) | nc;
						String k = HTML5_NAMED_REFS_VK.get(v);
						if (k != null) {
							// Named
							sb.append('&').append(k);
						} else {
							// Code point
							int cp = Character.toCodePoint(c, nc);
							if (useDecimalNumRef) {
								sb.append("&#").append(cp).append(';');
							} else {
								sb.append("&#x").append(Integer.toHexString(cp)).append(';');
							}
						}
						
						i++;
						continue;
					}
				}
			} else {
				if (i + 1 < len && HTML5_NAMED_REFS_2DIG.contains(c)) {
					char nc = str.charAt(i);
					
					int v = (((int)c) << 16) | nc;
					String k = HTML5_NAMED_REFS_VK.get(v);
					if (k != null) {
						// Named
						sb.append('&').append(k);
						
						i++;
						continue;
					}
				}

				String k = HTML5_NAMED_REFS_VK.get(Integer.valueOf((int)c));
				if (k != null) {
					// Named
					sb.append('&').append(k);
					
					continue;
				}
			}
			
			// Code point
			if (useDecimalNumRef) {
				sb.append("&#").append((int)c).append(';');
			} else {
				sb.append("&#x").append(Integer.toHexString((int)c)).append(';');
			}
		}
		
		return sb.toString();
	}
	
	public static final String unescapeHTML5(String str) {
		
		if (str == null || str.isEmpty()) {
			return str;
		}
		
		int len = str.length();
		
		int idx = 0;
		
		StringBuilder sb = new StringBuilder(str.length());
		do {
			int beginIdx = str.indexOf('&', idx);
			if (beginIdx == -1) {
				sb.append(str, idx, str.length());
				break;
			}
			
			sb.append(str, idx, beginIdx);
			
			int refIdx = beginIdx + 1;
			int endIdx;
			boolean matched = false;
			if((refIdx < len) && str.charAt(refIdx) == '#') {
				refIdx++;
				
				if ((refIdx < len) && (str.charAt(refIdx) == 'x' || str.charAt(refIdx) == 'X')) {
					// Hexadecimal numeric character references
					
					refIdx++;
					
					char c = '\0';
					for (endIdx = refIdx; endIdx < len; endIdx++) {
						c = str.charAt(endIdx);
						if (!(('0' <= c && c <= '9') || ('A' <= c && c <= 'F') || ('a' <= c && c <= 'f'))) {
							break;
						}
					}
					String ref = str.substring(refIdx, endIdx);
					
					if (c == ';') {
						endIdx++;
					}
					
					if (0 < ref.length()) {
						try {
							int cp = Integer.parseInt(ref, 16);
							if (Character.isDefined(cp)) {
								sb.appendCodePoint(cp);
								matched = true;
							}
						} catch (NumberFormatException e) {
							// NOP
						}
					}
				} else {
					// Decimal numeric character references
					
					char c = '\0';
					for (endIdx = refIdx; endIdx < len; endIdx++) {
						c = str.charAt(endIdx);
						if (!('0' <= c && c <= '9')) {
							break;
						}
					}
					String ref = str.substring(refIdx, endIdx);
					
					if (c == ';') {
						endIdx++;
					}
					
					if (0 < ref.length()) {
						try {
							int cp = Integer.parseInt(ref);
							if (Character.isDefined(cp)) {
								sb.appendCodePoint(cp);
								matched = true;
							}
						} catch (NumberFormatException e) {
							// NOP
						}
					}
				}
			} else {
				// Named character references
				
				for (endIdx = refIdx; endIdx < len; endIdx++) {
					char c = str.charAt(endIdx);
					if (!(('A' <= c && c <= 'Z') || ('a' <= c && c <= 'z') || ('0' <= c && c <= '9'))) {
						if (c == ';') {
							endIdx++;
						}
						break;
					}
				}
				String ref = str.substring(refIdx, endIdx);
				
				if (0 < ref.length()) {
					String refStr = HTML5_NAMED_REFS.get(ref);
					if (refStr != null) {
						sb.append(refStr);
						matched = true;
					}
				}
			}
			
			if (!matched) {
				sb.append(str, beginIdx, endIdx);
			}
			
			idx = endIdx;
		} while (idx < len);
		
		return sb.toString();
	}
	
	public static final String br(String str) {
		if (str == null || str.isEmpty()) {
			return str;
		}
		
		StringBuilder sb = new StringBuilder(str.length() * 2);
		int maxIdx = str.length() - 1;
		for (int i = 0; i <= maxIdx; i++) {
			char c = str.charAt(i);
			switch (c) {
			case '\r':
				sb.append("<br />");
				if (i != maxIdx) {
					char nc = str.charAt(i + 1);
					if (nc == '\n') {
						i++;
					}
				}
				break;
			case '\n':
				sb.append("<br />");
				break;
			default:
				sb.append(c);
				break;
			}
		}
		
		return sb.toString();
	}

	public static final String escapeBasicHTML(String str, boolean escapeNbsp, boolean onlyHTML4Ref) {
		if (str == null || str.isEmpty()) {
			return str;
		}
		
		int len = str.length();
		
		StringBuilder sb = new StringBuilder(len * 2);
		for (int i = 0; i < len; i++) {
			char c = str.charAt(i);
			
			switch (c) {
			case '<': sb.append("&lt;"); break;
			case '>': sb.append("&gt;"); break;
			case '&': sb.append("&amp;"); break;
			case '"': sb.append("&quot;"); break;
			case '\'': sb.append(onlyHTML4Ref ? "&#39;" : "&apos;"); break;
			case ' ': sb.append(escapeNbsp ? "&nbsp;" : " "); break;
			case '\u00A0': sb.append(escapeNbsp ? "&nbsp;" : "\u00A0"); break;
			default: sb.append(c); break;
			}
		}
		
		return sb.toString();
	}
	
